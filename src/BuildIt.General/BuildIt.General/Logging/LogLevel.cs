namespace BuildIt.Logging
{
    /// <summary>
    /// The different log levels
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// No log level - don't use
        /// </summary>
        None,

        /// <summary>
        /// Maximum detail
        /// </summary>
        Verbose,

        /// <summary>
        /// Information level
        /// </summary>
        Information,

        /// <summary>
        /// Exception level
        /// </summary>
        Error
    }
}